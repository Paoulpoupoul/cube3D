#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*lst;

	lst = malloc(sizeof(*lst));
	if (!lst)
		return (NULL);
	if (!content)
		lst -> content = NULL;
	else
		lst -> content = content;
	lst -> next = NULL;
	return (lst);
}
